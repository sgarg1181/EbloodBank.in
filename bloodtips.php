    <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse2">Blood Groups</a>
        </h4>
      </div>
      <div id="collapse2" class="panel-collapse collapse">
        <div class="panel-body">Blood type is determined by which antibodies and antigens the person's blood produces. An individual has two types of blood groups namely ABO-grouping and Rh-grouping. Rh is called as the Rhesus factor that has come to us from Rhesus monkeys.<br><br>
          Most humans are in the ABO blood group. The ABO group has four categories namely<br> 
1) A group 2) B group 3) O group and 4) AB group<br>
In the Rh- group, either the individual is said to be Rh- Negative or Rh- Positive.<br><br>
    Thus blood group of any human being will mainly fall in any one of the following groups. <br>
A positive or A negative <br>
B positive or B negative <br>
O positive or O negative <br>
AB positive or AB negative.<br>
          </div>
      </div>
    </div>
    <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">Universal Donor and Recipients</a>
        </h4>
      </div>
      <div id="collapse3" class="panel-collapse collapse">
        <div class="panel-body">The most common blood type is O, followed by type A.<br><br>

Type O individuals are often called "universal donors" since their blood can be transfused into persons with any blood type. Those with type AB blood are called "universal recipients" because they can receive blood of any type.<br><br>

However, since approximately twice as many people in the general population have O and A blood types, a blood bank's need for this type of blood increases exponentially.</div>
      </div>
    </div>
  <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">Do Donate Blood, Only if you satisfy all the following conditions</a>
        </h4>
      </div>
      <div id="collapse4" class="panel-collapse collapse">
        <div class="panel-body"><ul>
            <li>You are between age group of 18-60 years.</li>
            <li> Your weight is 45 kgs or more.</li>
            <li>Your hemoglobin is 12.5 gm% minimum.</li>
            <li>Your last blood donation was 3 or more months earlier.</li>
            <li>You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past.</li>
            </ul></div>
      </div>
    </div>
      <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse5">DO NOT donate blood, if you have any of the following conditions</a>
        </h4>
      </div>
      <div id="collapse5" class="panel-collapse collapse">
        <div class="panel-body"><ul>
           <li> Cold / fever in the past 1 week.
            <li>Under treatment with antibiotics or any other medication.</li>
            <li>Cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer, chronic kidney or liver disease, bleeding tendencies, venereal disease etc.</li>
            <li>Major surgery in the last 6 months.</li>
            <li>Vaccination in the last 24 hours.</li>
            <li>Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.</li>
            <li>Had fainting attacks during last donation.</li>
<li>Have regularly received treatment with blood products.</li>
            <li>Shared a needle to inject drugs/ have history of drug addiction.</li>
            <li>Had sexual relations with different partners or with a high risk individual.</li>
          <li>Been tested positive for antibodies to HIV.</li>
            </ul></div>
      </div>
