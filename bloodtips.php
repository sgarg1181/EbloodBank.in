        </h4>
      </div>
      <div id="collapse1" class="panel-collapse collapse in">
        <div class="panel-body">Donating blood is safe and simple. It takes approximately 10-15 minutes to complete the blood donation process. Any healthy adult between 18 years and 60 years of age can donate blood. This is what you can expect when you are ready to donate blood:
          <ul>
            <li>You walk into a reputed and safe blood donation centre or a mobile camp organized by a reputed institution.</li>
              <li>
A few questions will be asked to determine your health status (general questions on health, donation history etc). Usually you will be asked to fill out a short form.</li>
              <li>
Then a quick physical check will be done to check temperature, blood pressure, pulse and hemoglobin content in blood to ensure you are a healthy donor.</li>
              <li>
If found fit to donate, then you will be asked to lie down on a resting chair or a bed. Your arm will be thoroughly cleaned. Then using sterile equipments blood will be collected in a special plastic bag. Approximately 350 ml of blood will be collected in one donation. Those who weigh more than 60 Kg can donate 450 ml of blood.</li>
              <li>
Then you must rest and relax for a few minutes with a light snack and something refreshing to drink. Some snacks and juice will be provided.</li>
              <li>
Blood will be separated into components within eight hours of donation
                  The blood will then be taken to the laboratory for testing.</li><li>
Once found safe, it will be kept in special storage and released when required.
The blood is now ready to be taken to the hospital, to save lives.</li>
            
            </ul>
          
          </div>
      </div>
